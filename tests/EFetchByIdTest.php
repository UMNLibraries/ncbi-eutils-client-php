  function testFetchById()
  {
    $fileSet = new DateSequence(array(
      'directory' => dirname(__FILE__) . '/fixtures/fetched-by-id',
      'suffix' => '.xml',
    ));

    // Clean out any already-existing files, e.g. from previous test runs:
    $fileSet->clear();

    $ids = $this->ids();

    $ef = new EFetchById(array(
      'email' => getenv('NCBI_USER_EMAIL'),
      'db'    => 'pubmed',
      'tool'  => getenv('NCBI_USER_TOOL'),
      'rettype' => 'citation',
      'retmode' => 'xml',
      'fileSet' => $fileSet,
      'ids' => $ids,
    ));
    $filenames = $ef->fetch();

    $downloadCount = 0;
    foreach ($filenames as $filename) {
      $xml = simplexml_load_file($filename);
      $fileCount = count(array_keys($xml->xpath('//PubmedArticle')));
      // Re-enable this after adding support for a recordsPerDownload property:
      //$this->assertEquals($this->recordsPerDownload, $fileCount);
      $downloadCount += $fileCount;
    }
    $this->assertEquals(count($ids), $downloadCount);

    // Cleanup:
    $fileSet->clear();
  }

